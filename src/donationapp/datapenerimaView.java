    public void loadData(){
        nama = txtNama.getText();
        alamat=txtAlamat.getText();
        kontak=txtKontak.getText();
    }
        
   public void saveData(){
        loadData();
        try {
            Statement stat = (Statement) db.koneksi.getKoneksi().createStatement();
            String sql = "insert into tb_penerima() values (null, '"+nama+"', '"+alamat+"','"+kontak+"')";
            
            PreparedStatement p = (PreparedStatement) db.koneksi.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            getData();
            reset();
            JOptionPane.showMessageDialog(null, "Berhasil");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        reset();
    }
         public void reset(){
            nama ="";
            alamat = "";
            kontak = "";
            
            
            txtNama.setText(nama);
            txtAlamat.setText(alamat);
            txtKontak.setText(kontak);
            
    }
     public void dataSelect(){
        int i = tblPenerima.getSelectedRow();
        if(i==-1){
            return;
        }
        
        idPenerima = model.getValueAt(i, 0).toString();
        txtNama.setText(""+model.getValueAt(i, 1));
        txtAlamat.setText(""+model.getValueAt(i, 2));
        txtKontak.setText(""+model.getValueAt(i, 3));
    }
    
    public void updateData(){
        loadData();
        
        try {
            Statement stat = (Statement) db.koneksi.getKoneksi().createStatement();
            String sql = "update tb_penerima set nama = '"+nama+"',alamat = '"+alamat+"',kontak = '"+kontak+"' where id_penerima = '"+idPenerima+"'";
            
            PreparedStatement p = (PreparedStatement) db.koneksi.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            getData();
            
            reset();
            JOptionPane.showMessageDialog(null, "Update Berhasil");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
    public void deleteData(){
        loadData();
        
        int pesan = JOptionPane.showConfirmDialog(null, "Anda yakin menghapus data "+nama+"?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION);
        if(pesan == JOptionPane.OK_OPTION){
            try {
                Statement stat = (Statement) db.koneksi.getKoneksi().createStatement();
                String sql = "delete from tb_penerima where id_penerima = '"+idPenerima+"'";

                PreparedStatement p = (PreparedStatement) db.koneksi.getKoneksi().prepareStatement(sql);
                p.executeUpdate();
                getData();

                reset();
                JOptionPane.showMessageDialog(null, "Delete Berhasil");
            } catch (SQLException e) {
              JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }
