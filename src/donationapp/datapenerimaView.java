    public void getData(){
        //remove data 
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            //membuat state pemanggilan data
            Statement stat = (Statement) db.koneksi.getKoneksi().createStatement();
            String sql = "Select * from tb_penerima";
            ResultSet res = stat.executeQuery(sql);
            
            while(res.next()){
                Object[] obj = new Object[4];
                
                obj[0] = res.getString("id_penerima");
                obj[1] = res.getString("nama");
                obj[2] = res.getString("alamat");
                obj[3] = res.getString("kontak");
                
                model.addRow(obj);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
   
